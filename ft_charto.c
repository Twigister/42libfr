int	ft_toupper(int c)
{
	if ('a' <= c && c <= 'z')
		return (c - 32);
	return ((unsigned char)c);
}

int	ft_tolower(int c)
{
	return (c + ('A' <= c && c <= 'Z') * 32);
}
